def levelUp(author,cXp):
        conn = sqlite3.connect('item_storage.sqlite')
        c = conn.cursor() 
        c.execute("SELECT XP FROM {tn} WHERE {cn}=('{u}')".\
            format(tn=table_name2,cn="user1",u=author))
